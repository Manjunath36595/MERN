// Arithmetic Operators
let a = 4;
let b = 8;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
// Assignment Operators
let d = 5;
d += 3;
console.log(d);
d -= 2;
console.log(d);
d *= 4;
console.log(d);
d /= 2;
console.log(d);
d %= 3;
console.log(d);
d **= 2;
console.log(d);
// Comparison Operators
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);
// Logical Operators
let x = true;
let y = false;
console.log(x && y);
console.log(x || y);
console.log(!x);
// Ternary Operator
let age = 20;
let result = age >= 18 ? "Adult" : "Minor";
console.log(result);
